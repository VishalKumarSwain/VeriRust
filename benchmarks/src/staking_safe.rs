pub type Pubkey = u32;

// @account
pub struct StakingPool {
    pub authority: Pubkey,
    pub total_staked: u64,
    pub reward_rate: u64,   // rewards per unit staked (scaled by 1000)
}

// @account
pub struct UserStake {
    pub user: Pubkey,
    pub staked_amount: u64,
    pub rewards_earned: u64,
}

// @context
pub struct StakeCtx {
    pub pool: StakingPool,
    pub user_stake: UserStake,
    pub user: Pubkey,
    pub is_signer: bool,
}

// @context
pub struct UnstakeCtx {
    pub pool: StakingPool,
    pub user_stake: UserStake,
    pub user: Pubkey,
    pub is_signer: bool,
}

// @context
pub struct ClaimCtx {
    pub pool: StakingPool,
    pub user_stake: UserStake,
    pub user: Pubkey,
    pub is_signer: bool,
}

pub fn stake(ctx: &mut StakeCtx, amount: u64) -> Result<(), &'static str> {
    if !ctx.is_signer {
        return Err("not signer");
    }
    if ctx.user_stake.user != ctx.user {
        return Err("unauthorized");
    }
    ctx.user_stake.staked_amount = ctx.user_stake.staked_amount
        .checked_add(amount).ok_or("overflow")?;
    ctx.pool.total_staked = ctx.pool.total_staked
        .checked_add(amount).ok_or("overflow")?;
    Ok(())
}

pub fn unstake(ctx: &mut UnstakeCtx, amount: u64) -> Result<(), &'static str> {
    if !ctx.is_signer {
        return Err("not signer");
    }
    if ctx.user_stake.user != ctx.user {
        return Err("unauthorized");
    }
    if ctx.user_stake.staked_amount < amount {
        return Err("insufficient staked");
    }
    ctx.user_stake.staked_amount -= amount;
    ctx.pool.total_staked = ctx.pool.total_staked
        .checked_sub(amount).ok_or("underflow")?;
    Ok(())
}

pub fn claim_rewards(ctx: &mut ClaimCtx) -> Result<u64, &'static str> {
    if !ctx.is_signer {
        return Err("not signer");
    }
    if ctx.user_stake.user != ctx.user {
        return Err("unauthorized");
    }
    let rewards = ctx.user_stake.rewards_earned;
    ctx.user_stake.rewards_earned = 0;
    Ok(rewards)
}

#[cfg(test)]
mod tests {
    use super::*;

    fn make_pool() -> StakingPool {
        StakingPool { authority: 1, total_staked: 0, reward_rate: 10 }
    }

    fn make_user_stake(user: Pubkey) -> UserStake {
        UserStake { user, staked_amount: 0, rewards_earned: 0 }
    }

    #[test]
    fn test_stake_ok() {
        let mut ctx = StakeCtx {
            pool: make_pool(),
            user_stake: make_user_stake(2),
            user: 2,
            is_signer: true,
        };
        assert!(stake(&mut ctx, 100).is_ok());
        assert_eq!(ctx.user_stake.staked_amount, 100);
        assert_eq!(ctx.pool.total_staked, 100);
    }

    #[test]
    fn test_unstake_ok() {
        let mut ctx = UnstakeCtx {
            pool: StakingPool { authority: 1, total_staked: 100, reward_rate: 10 },
            user_stake: UserStake { user: 2, staked_amount: 100, rewards_earned: 0 },
            user: 2,
            is_signer: true,
        };
        assert!(unstake(&mut ctx, 50).is_ok());
        assert_eq!(ctx.user_stake.staked_amount, 50);
        assert_eq!(ctx.pool.total_staked, 50);
    }

    #[test]
    fn test_unauthorized_unstake() {
        let mut ctx = UnstakeCtx {
            pool: StakingPool { authority: 1, total_staked: 100, reward_rate: 10 },
            user_stake: UserStake { user: 2, staked_amount: 100, rewards_earned: 0 },
            user: 99,   // wrong user
            is_signer: true,
        };
        assert_eq!(unstake(&mut ctx, 10), Err("unauthorized"));
    }
}

