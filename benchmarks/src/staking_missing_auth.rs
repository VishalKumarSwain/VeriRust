// VeriRust Benchmark: staking_missing_auth
// Bug: MISSING_AUTH — unstake does not verify user identity
// Expected Kani result: FAIL on verify_staking_auth_unstake
//   Counterexample: user_stake.user != user, yet unstake returns Ok

pub type Pubkey = u32;

// @account
pub struct StakingPool {
    pub authority: Pubkey,
    pub total_staked: u64,
}

// @account
pub struct UserStake {
    pub user: Pubkey,
    pub staked_amount: u64,
}

// @context
pub struct UnstakeCtx {
    pub pool: StakingPool,
    pub user_stake: UserStake,
    pub user: Pubkey,
    pub is_signer: bool,
}

pub fn unstake(ctx: &mut UnstakeCtx, amount: u64) -> Result<(), &'static str> {
    // BUG: missing identity check — any signer can unstake any user's stake
    // Should be: if ctx.user_stake.user != ctx.user { return Err("unauthorized"); }
    if ctx.user_stake.staked_amount < amount {
        return Err("insufficient staked");
    }
    ctx.user_stake.staked_amount -= amount;
    ctx.pool.total_staked = ctx.pool.total_staked
        .checked_sub(amount).ok_or("underflow")?;
    Ok(())
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_unstake_happy_path() {
        let mut ctx = UnstakeCtx {
            pool: StakingPool { authority: 1, total_staked: 100 },
            user_stake: UserStake { user: 2, staked_amount: 100 },
            user: 2,
            is_signer: true,
        };
        assert!(unstake(&mut ctx, 50).is_ok());
    }

    // Unit test only covers matching user — the auth bug is NOT caught here
}

