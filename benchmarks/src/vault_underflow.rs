// VeriRust Benchmark: vault_underflow
// Bug: UNDERFLOW — withdraw subtracts without checking balance >= amount
// Expected Kani result: FAIL on verify_vault_underflow_withdraw
//   Counterexample: balance=0, amount=1 → panic (u64 underflow)

pub type Pubkey = u32;

// @account
pub struct VaultState {
    pub authority: Pubkey,
    pub balance: u64,
}

// @context
pub struct WithdrawCtx {
    pub vault: VaultState,
    pub authority: Pubkey,
    pub is_signer: bool,
}

pub fn withdraw(ctx: &mut WithdrawCtx, amount: u64) -> Result<(), &'static str> {
    if !ctx.is_signer {
        return Err("not signer");
    }
    if ctx.vault.authority != ctx.authority {
        return Err("unauthorized");
    }
    // BUG: missing balance check — panics when amount > vault.balance
    ctx.vault.balance -= amount;
    Ok(())
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_withdraw_happy_path() {
        let mut ctx = WithdrawCtx {
            vault: VaultState { authority: 1, balance: 100 },
            authority: 1,
            is_signer: true,
        };
        assert!(withdraw(&mut ctx, 50).is_ok());
        assert_eq!(ctx.vault.balance, 50);
    }

    // Unit tests only cover happy path — the underflow bug is NOT caught here
}

