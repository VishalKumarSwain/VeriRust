// VeriRust Benchmark: token_mint_bug
// Bug: SUPPLY_INVARIANT — burn reduces user balance but NOT total_supply
// Expected Kani result: FAIL on verify_token_burn_supply
//   Counterexample: total_supply unchanged after successful burn

pub type Pubkey = u32;

// @account
pub struct MintState {
    pub authority: Pubkey,
    pub total_supply: u64,
}

// @account
pub struct UserAccount {
    pub user: Pubkey,
    pub balance: u64,
}

// @context
pub struct MintCtx {
    pub mint: MintState,
    pub user_account: UserAccount,
    pub authority: Pubkey,
    pub is_signer: bool,
}

// @context
pub struct BurnCtx {
    pub mint: MintState,
    pub user_account: UserAccount,
    pub user: Pubkey,
    pub is_signer: bool,
}

pub fn mint_tokens(ctx: &mut MintCtx, amount: u64) -> Result<(), &'static str> {
    if !ctx.is_signer {
        return Err("not signer");
    }
    if ctx.mint.authority != ctx.authority {
        return Err("unauthorized");
    }
    ctx.mint.total_supply = ctx.mint.total_supply
        .checked_add(amount).ok_or("overflow")?;
    ctx.user_account.balance = ctx.user_account.balance
        .checked_add(amount).ok_or("overflow")?;
    Ok(())
}

pub fn burn(ctx: &mut BurnCtx, amount: u64) -> Result<(), &'static str> {
    if !ctx.is_signer {
        return Err("not signer");
    }
    if ctx.user_account.user != ctx.user {
        return Err("unauthorized");
    }
    if ctx.user_account.balance < amount {
        return Err("insufficient balance");
    }
    ctx.user_account.balance -= amount;
    // BUG: total_supply is never reduced — supply invariant violated
    // Missing: ctx.mint.total_supply -= amount;
    Ok(())
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_burn_happy_path() {
        let mut ctx = BurnCtx {
            mint: MintState { authority: 1, total_supply: 1000 },
            user_account: UserAccount { user: 2, balance: 100 },
            user: 2,
            is_signer: true,
        };
        assert!(burn(&mut ctx, 50).is_ok());
        assert_eq!(ctx.user_account.balance, 50);
        // Unit test does NOT check total_supply — bug goes undetected
    }
}

