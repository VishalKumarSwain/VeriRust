// VeriRust Benchmark: vault_overflow
// Bug: OVERFLOW — deposit uses += without overflow check
// Expected Kani result: FAIL on verify_vault_overflow_deposit
//   Counterexample: balance=u64::MAX, amount=1 → panic (u64 overflow)

pub type Pubkey = u32;

// @account
pub struct VaultState {
    pub authority: Pubkey,
    pub balance: u64,
}

// @context
pub struct DepositCtx {
    pub vault: VaultState,
    pub authority: Pubkey,
    pub is_signer: bool,
}

pub fn deposit(ctx: &mut DepositCtx, amount: u64) -> Result<(), &'static str> {
    if !ctx.is_signer {
        return Err("not signer");
    }
    if ctx.vault.authority != ctx.authority {
        return Err("unauthorized");
    }
    // BUG: unchecked addition — panics when balance + amount > u64::MAX
    ctx.vault.balance += amount;
    Ok(())
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_deposit_happy_path() {
        let mut ctx = DepositCtx {
            vault: VaultState { authority: 1, balance: 100 },
            authority: 1,
            is_signer: true,
        };
        assert!(deposit(&mut ctx, 50).is_ok());
        assert_eq!(ctx.vault.balance, 150);
    }

    // Unit tests do not cover extreme values — overflow bug is NOT caught here
}

