pub type Pubkey = u32;

// @account
pub struct VaultState {
    pub authority: Pubkey,
    pub balance: u64,
}

// @context
pub struct DepositCtx {
    pub vault: VaultState,
    pub authority: Pubkey,
    pub is_signer: bool,
}

// @context
pub struct WithdrawCtx {
    pub vault: VaultState,
    pub authority: Pubkey,
    pub is_signer: bool,
}

pub fn deposit(ctx: &mut DepositCtx, amount: u64) -> Result<(), &'static str> {
    if !ctx.is_signer {
        return Err("not signer");
    }
    if ctx.vault.authority != ctx.authority {
        return Err("unauthorized");
    }
    ctx.vault.balance = ctx.vault.balance.checked_add(amount).ok_or("overflow")?;
    Ok(())
}

pub fn withdraw(ctx: &mut WithdrawCtx, amount: u64) -> Result<(), &'static str> {
    if !ctx.is_signer {
        return Err("not signer");
    }
    if ctx.vault.authority != ctx.authority {
        return Err("unauthorized");
    }
    if ctx.vault.balance < amount {
        return Err("insufficient funds");
    }
    ctx.vault.balance -= amount;
    Ok(())
}

#[cfg(test)]
mod tests {
    use super::*;

    fn make_vault(authority: Pubkey, balance: u64) -> VaultState {
        VaultState { authority, balance }
    }

    #[test]
    fn test_deposit_ok() {
        let mut ctx = DepositCtx {
            vault: make_vault(1, 100),
            authority: 1,
            is_signer: true,
        };
        assert!(deposit(&mut ctx, 50).is_ok());
        assert_eq!(ctx.vault.balance, 150);
    }

    #[test]
    fn test_withdraw_ok() {
        let mut ctx = WithdrawCtx {
            vault: make_vault(1, 100),
            authority: 1,
            is_signer: true,
        };
        assert!(withdraw(&mut ctx, 50).is_ok());
        assert_eq!(ctx.vault.balance, 50);
    }

    #[test]
    fn test_withdraw_insufficient() {
        let mut ctx = WithdrawCtx {
            vault: make_vault(1, 10),
            authority: 1,
            is_signer: true,
        };
        assert_eq!(withdraw(&mut ctx, 50), Err("insufficient funds"));
    }

    #[test]
    fn test_unauthorized() {
        let mut ctx = WithdrawCtx {
            vault: make_vault(1, 100),
            authority: 2,
            is_signer: true,
        };
        assert_eq!(withdraw(&mut ctx, 10), Err("unauthorized"));
    }
}

